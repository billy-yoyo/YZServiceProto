<p>
    <b>What is the purpose of this study? </b>
</p>

<p>
    This PhD research project aims to develop and validate digital service proposals for UK HEIs to support and facilitate the intercultural adaptation process, especially for Chinese postgraduate taught (PGT) students. The success (or effectiveness and usefulness) of the digital service proposals could be measured by the feedback from the Chinese PGT students. The main contribution of this research project would be to deliver the proven digital service proposals as a useful guide and reference for service design in HEIs regarding intercultural adaptation of Chinese (and other) international PGT students (or other students participating in other types of short-term study programmes of less than two years) in the UK and beyond.
</p>

<p>
    The research for this study is being undertaken by Yi Zhong who is a research student in the Faculty of Arts, Design and Humanities at De Montfort University, Leicester.  
</p>

